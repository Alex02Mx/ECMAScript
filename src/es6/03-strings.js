// modo tradicional concatenando
let hello = "Hello";
let world = "World";
let epicPhrase = hello + " " + world;
console.log(epicPhrase);

// template literals con comillas francesas
let epicPhrase2 = `${hello} ${world} !`;
console.log(epicPhrase2);

// multi line strings
let lorem = "este es un string \n" + "este es otro string";
console.log(lorem);

let lorem2 = `este es un string con ecmas6 
este es otro string con ecmas6`;
console.log(lorem2);